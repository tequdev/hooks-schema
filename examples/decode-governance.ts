const decoded = namespaceEntries.map((entry) => {
  const result = decodeState(schema, {
    key: entry.HookStateKey,
    value: entry.HookStateData,
  });

  return {
    index: entry.index,
    state: result.state,
    name: result.metadata.name,
    key: result.key,
    value: result.value,
  };
});

console.log(JSON.stringify({ decodedCount: decoded.length, decoded }, jsonReplacer, 2));
